export type Messages = (typeof messages)[Locale]

export function detectLocale(cookieLocale?: string | null, acceptLanguage?: string | null): Locale {
  if (cookieLocale === 'ko' || cookieLocale === 'en') {
    return cookieLocale
  }

  return acceptLanguage?.toLowerCase().includes('ko') ? 'ko' : defaultLocale
}

export function getIntlLocale(locale: Locale) {
  return locale === 'ko' ? 'ko-KR' : 'en-US'
}

export function translateCategory(category: string, locale: Locale) {
  return (
    messages[locale].common.categories[
      category as keyof (typeof messages)[typeof defaultLocale]['common']['categories']
    ] ?? category
  )
}

export function translateRole(role: string, locale: Locale) {
  return messages[locale].common.roles[role as keyof (typeof messages)[typeof defaultLocale]['common']['roles']] ?? role
}

export function translateProductDescription(description: string, locale: Locale) {
  return productDescriptionTranslations[locale][description] ?? description
}

export function formatDate(locale: Locale, value: string) {
  return new Intl.DateTimeFormat(getIntlLocale(locale)).format(new Date(value))
}

export function formatDateTime(locale: Locale, value: string) {
  return new Intl.DateTimeFormat(getIntlLocale(locale), {
    dateStyle: 'medium',
    timeStyle: 'short',
  }).format(new Date(value))
}
